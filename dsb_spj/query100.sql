SELECT min(item1.i_item_sk),
       min(item2.i_item_sk),
       min(s1.ss_ticket_number),
       min(s1.ss_item_sk)
FROM item AS item1,
     item AS item2,
     store_sales AS s1,
     store_sales AS s2,
     date_dim,
     customer,
     customer_address,
     customer_demographics
WHERE item1.i_item_sk < item2.i_item_sk
  AND s1.ss_ticket_number = s2.ss_ticket_number
  AND s1.ss_item_sk = item1.i_item_sk 
  AND s2.ss_item_sk = item2.i_item_sk
  AND s1.ss_customer_sk = c_customer_sk
  AND c_current_addr_sk = ca_address_sk
  AND c_current_cdemo_sk = cd_demo_sk
  AND d_year BETWEEN 1999 AND 2000                    
  AND d_date_sk = s1.ss_sold_date_sk
  AND item1.i_category IN ('Electronics', 'Sports')    
  AND item2.i_manager_id BETWEEN 50 AND 70            
  AND cd_marital_status = 'M'                          
  AND cd_education_status = 'College'               
  AND s1.ss_list_price BETWEEN 177.5 AND 182.5       
  AND s2.ss_list_price BETWEEN 177.5 AND 182.5      